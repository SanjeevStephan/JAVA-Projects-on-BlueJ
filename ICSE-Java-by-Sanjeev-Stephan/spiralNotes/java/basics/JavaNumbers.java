package spiralNotes.java.basics;

public class JavaNumbers {
    public static void main(String[] args) {
        byte myByte = 127; // byte can store number only between -128 to 127
        short myShort = 32767;  // short can store number only between - 32768 to 32767
        int myInt = 1234567899; // int can store number ranging upto 10 digits
        long myLong = 1500000000000L;  // long can store number ranging upto 19 digits
        float myFloat = 5.75f;  // float values always ends with 'f'
        double myDouble = 19.199; // use double for precision of about 16 decimal digits
    }
}
