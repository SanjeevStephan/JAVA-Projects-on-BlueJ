// Java program to print the value from 1 to 5
package spiralNotes.java.basics;

public class JavaWhileLoop {
    public static void main(String[] args)
    {
        int i = 1;          // initialization
        while(i <=5)       // condition
        {
            System.out.println(i);
            i++;        // update (increment)
        }
    }
}
